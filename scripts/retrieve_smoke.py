from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from src.retrieval.bm25 import BM25Retriever
from src.retrieval.faiss_dense import FaissDenseRetriever
from src.retrieval.hybrid import HybridRetriever


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Smoke test BM25/FAISS retrieval from built index artifacts.")
    parser.add_argument(
        "--manifest",
        type=Path,
        default=Path("data/indexes/wiki_passages/manifest.json"),
        help="Path to index manifest generated by build_retrieval_indexes.py",
    )
    parser.add_argument("--query", type=str, default="Who is the president of the United States?")
    parser.add_argument("--top-k", type=int, default=3)
    parser.add_argument("--hybrid-alpha", type=float, default=0.5)
    parser.add_argument("--hybrid-rrf-k", type=int, default=60)
    parser.add_argument("--hybrid-candidate-k", type=int, default=50)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    with args.manifest.open("r", encoding="utf-8") as f:
        m = json.load(f)

    print(f"Query: {args.query}")
    print()

    bm25 = None
    dense = None

    if "bm25_path" in m:
        print("=== BM25 ===")
        bm25 = BM25Retriever(m["bm25_path"], m["docstore_path"])
        for i, doc in enumerate(bm25.retrieve(args.query, args.top_k), start=1):
            print(f"{i}. [{doc.doc_id}] {doc.title} :: {doc.text[:180]}")
        print()

    if "faiss_index_path" in m and "dense_config_path" in m:
        print("=== Dense (FAISS) ===")
        dense = FaissDenseRetriever(m["faiss_index_path"], m["docstore_path"], m["dense_config_path"])
        for i, doc in enumerate(dense.retrieve(args.query, args.top_k), start=1):
            print(f"{i}. [{doc.doc_id}] {doc.title} :: {doc.text[:180]}")
        print()

    if bm25 is not None and dense is not None:
        print("=== Hybrid (RRF) ===")
        hybrid = HybridRetriever(
            bm25_retriever=bm25,
            dense_retriever=dense,
            alpha=args.hybrid_alpha,
            rrf_k=args.hybrid_rrf_k,
            candidate_k=args.hybrid_candidate_k,
        )
        for i, doc in enumerate(hybrid.retrieve(args.query, args.top_k), start=1):
            print(f"{i}. [{doc.doc_id}] {doc.title} :: {doc.text[:180]}")


if __name__ == "__main__":
    main()
